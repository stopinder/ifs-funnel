<h1>Privacy Policy</h1>

<p><strong>Chrysalis Therapy Services</strong> is operated by Robert Ormiston, Registered Member MBACP (Membership No. 00948196), providing psychotherapy and reflective services to clients in the UK.</p>

<h2>1. Information Collected</h2>
<p>Personal data may include name, contact information, date of birth, referral source, session notes, and other sensitive data relevant to therapeutic assessment and treatment.</p>

<h2>2. Purpose of Data Collection</h2>
<p>Data is collected to deliver psychotherapy services, manage appointments, provide reflective reports, and meet legal and professional obligations.</p>

<h2>3. Lawful Basis</h2>
<p>Data is processed under the lawful bases of consent, contract, and legitimate interest. For special category data (e.g. health-related info), processing is based on the provision of health care as defined under Article 9(2)(h) of UK GDPR.</p>

<h2>4. Confidentiality & Security</h2>
<p>All client data is stored securely, either digitally (with encrypted storage) or physically (in locked cabinets). Confidentiality is maintained in line with the BACP Ethical Framework and UK GDPR standards.</p>

<h2>5. Use of AI</h2>
<p>Reflective report drafts may use AI tools (e.g. language models like GPT) under therapist supervision. No identifying personal data is shared with external services. All outputs are reviewed and authored by the therapist.</p>

<h2>6. Analytics & Cookies</h2>
<p>This website uses privacy-focused analytics tools to understand traffic patterns. No tracking cookies or personal identifiers are used.</p>

<h2>7. Data Retention</h2>
<p>Client records are retained for up to 7 years after the end of therapy, in line with guidance from professional bodies and insurers. Data may be retained longer if legally required.</p>

<h2>8. Your Rights</h2>
<p>Under the UK GDPR, you have the right to access, rectify, erase, restrict, or object to the processing of your personal data. You may also request data portability.</p>

<h2>9. Complaints</h2>
<p>If you have concerns about your data, contact <a href="mailto:info@chrysaliscounselling.uk">info@chrysaliscounselling.uk</a>. If unresolved, you may contact the Information Commissioner's Office (ICO): <a href="https://ico.org.uk">https://ico.org.uk</a>.</p>

